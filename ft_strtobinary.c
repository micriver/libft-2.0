#include "libft.h"

void	ft_strtobinary(char *str)
{
	int		i;

	i = 0;
	while (str[i])
	{
		ft_chartobits(str[i]);
		if (str[i + 1] != '\0')
			ft_putchar(' ');
		i++;
	}
}
